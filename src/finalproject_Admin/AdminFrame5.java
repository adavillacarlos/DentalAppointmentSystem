    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String un = cmbPatient.getSelectedItem().toString();
        String op = cmbOperation.getSelectedItem().toString();
        String date =  ((JTextField) txtDate.getDateEditor().getUiComponent()).getText(); 
        int resp = JOptionPane.showConfirmDialog(this,"Are you sure you want to delete?","Confirm",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(resp == JOptionPane.YES_OPTION){
                if(dbh.checkRecords(un, date, op)){
                    if(dbh.deleteRecords(un,date,op)){
                        JOptionPane.showMessageDialog(this,"Delete successfully");
                    }
                } else {
                    JOptionPane.showMessageDialog(this,"Operation does not exists");
                }
            }
        
        showTable(un);
    }//GEN-LAST:event_btnDeleteActionPerformed

