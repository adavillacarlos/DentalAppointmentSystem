    private void showOperations(){
         try {
            String operation; 
            ResultSet rs = dbh.displayCmbOperation();
            while(rs.next()){
                operation = rs.getString("Operation");
                cmbOperation.addItem(operation);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminFrame5.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    private void showUsername(){
        try {
            String name; 
            ResultSet rs = dbh.displayUsers();
            while(rs.next()){
                name = rs.getString("Username");
                cmbPatient.addItem(name);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminFrame5.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
