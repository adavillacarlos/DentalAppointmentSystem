    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String un = cmbPatient.getSelectedItem().toString();
        String op = cmbOperation.getSelectedItem().toString();
        String date =  ((JTextField) txtDate.getDateEditor().getUiComponent()).getText(); 
        if(dbh.checkRecords(un, date, op)){
              if(dbh.updateRecords(un,date,op)){
                    JOptionPane.showMessageDialog(this,"Updated successfully");
                 }
        }
        showTable(un);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String un = cmbPatient.getSelectedItem().toString();
        String op = cmbOperation.getSelectedItem().toString();
        String date =  ((JTextField) txtDate.getDateEditor().getUiComponent()).getText(); 
        if(dbh.insertRecord(un,date,op)){
            JOptionPane.showMessageDialog(this,"Added up successfully");
        } else {
            JOptionPane.showMessageDialog(this,"Failed to add");
        }
        showTable(un);
    }//GEN-LAST:event_btnAddActionPerformed

