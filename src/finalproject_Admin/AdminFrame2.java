    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(cmbSearch.getSelectedIndex() != -1){
           try {
           String usertype = cmbSearch.getSelectedItem().toString();
            ResultSet rs = dbh.displayUsersByUsertype(usertype);
            while(tblUsers.getRowCount() > 0){
                ((DefaultTableModel) tblUsers.getModel()).removeRow(0);   
            }
            int columns = rs.getMetaData().getColumnCount();
            while(rs.next()){
                Object[] row = new Object[columns];
                for(int i = 1; i <=columns; i++){
                    row[i-1] = rs.getObject(i); 
                }
                ((DefaultTableModel) tblUsers.getModel()).insertRow(rs.getRow()-1, row);
            } 
        } catch (SQLException ex) {
             Logger.getLogger(AdminFrame4.class.getName()).log(Level.SEVERE, null, ex);
         }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int flag=0;
        String un = txtUn.getText().trim();
        String pass = txtPass.getText().trim();
        String email = txtEmail.getText().trim();
        String fname = txtFname.getText().trim(); 
        String lname = txtLname.getText().trim();  
        int index = cmbUt.getSelectedIndex(); 
        String ut="admin";
        if(index==0)
            ut="patient";
        
        if(dbh.checkUsername(un)){
                flag=1;
        }
        if(flag==1){
            if(dbh.updateProfile(un,pass,ut,fname,lname))
                JOptionPane.showMessageDialog(this,"Updated successfully.");
        } else {
                JOptionPane.showMessageDialog(this,"Username already exists");
        }
        showTable();
    }//GEN-LAST:event_btnEditActionPerformed

