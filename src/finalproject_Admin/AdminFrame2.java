    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String un = txtUn.getText().trim(); 
        if(un.isEmpty())
               JOptionPane.showMessageDialog(this,"Please answer all the fields");
        else {
            int resp = JOptionPane.showConfirmDialog(this,"Are you sure you want to delete?","Confirm",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(resp == JOptionPane.YES_OPTION){
                  if(dbh.checkUsername(un)==true){
                    if(dbh.deleteProfile(un)){
                        JOptionPane.showMessageDialog(this,"Delete successfully");
                    }
                    } else {
                    JOptionPane.showMessageDialog(this,"User does not exists");
                }
            }
          
        }
        showTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

